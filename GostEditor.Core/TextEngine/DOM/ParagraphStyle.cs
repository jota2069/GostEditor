namespace GostEditor.Core.TextEngine.DOM;

/// <summary>
/// Стили абзаца для разметки документа и построения оглавления.
/// </summary>
public enum ParagraphStyle
{
    Normal,     // Обычный текст (абзацный отступ, выравнивание по ширине)
    Heading1,   // Заголовок 1 уровня (ЖИРНЫЙ, ПО ЦЕНТРУ, КАПС) - например "ВВЕДЕНИЕ"
    Heading2,   // Заголовок 2 уровня (Жирный, с абзацного отступа) - например "1.1 Обзор литературы"
    Heading3,   // Заголовок 3 уровня
    Code        // Листинг кода (шрифт Consolas, без абзацного отступа)
}
